# 例2-12：利用*创建二维列表
board1 = [['_'] * 3 for i in range(3)]  # 创建一个 3x3 列表
board2 = [['_'] * 3] * 3
board3 = ['_'] * 3 * 3

board1[0][0] = [1]          # [1,'_','_'],['_','_','_'],['_','_','_']
board2[0][0] = 1            # [1,'_','_'],[1,'_','_'],[1,'_','_']
board3[0] = 1               # [1,'_','_','_','_','_','_','_','_']
