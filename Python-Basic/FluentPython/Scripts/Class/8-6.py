# 例8-6：浅复制
l1 = [3,[66,55,44],(7,8,9)]
l2 = l1[:]
l1.append(100)      # 在 l1 中追加元素，对 l2 没有影响
l1[1].remove(55)    # 在 l1 中删除 l1[1] 中的元素，这对 l2 有影响
print(l1)           # [3, [66, 44], (7, 8, 9), 100]
print(l2)           # [3, [66, 44], (7, 8, 9)]
l2[1] += [33,22]    # 对可变对象来说，+= 表示就地修改，这样的修改会影响到 l1
l2[2] += (10,11)    # 对元组这个扁平对象，他的修改会创新新的元组重新绑定给 l2 不会影响 l1
print(l1)           # [3, [66, 44, 33, 22], (7, 8, 9), 100]
print(l2)           # [3, [66, 44, 33, 22], (7, 8, 9, 10, 11)]